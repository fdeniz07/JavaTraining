package conditionals;

public class Main {

	public static void main(String[] args) {
		// Conditionals

		int number = 20;

		if (number < 20) {
			System.out.println("Number is smaller than 20");
		} else if (number == 20) {
			System.out.println("Number is equals 20");
		} else {
			System.out.println("Number is bigger than 20");
		}

	}

}
